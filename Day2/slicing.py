name = "Krishnaprasad"

print(name[:]) # form start to end
print(name[:5]) # from start(inclusive) to 4th index 5th is exclusive
print(name[5:]) # from 5th(inclusive) index to end of the string
print(name[5:9]) # from 5th(inclusive) index to 8th index 9th(exclusive)

# [start : end] -->> end is Exclusive